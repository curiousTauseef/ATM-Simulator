    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        
        sign_in si = new sign_in();
        try
        {    
            PreparedStatement stmt = c.prepareStatement("select * from data where cardno = '"+si.cardno+"' and pin ='"+si.pin+"'");
            
            ResultSet abc = stmt.executeQuery();
            abc.next();                    
            
            
            JFrame f = new JFrame();
            JOptionPane.showMessageDialog(f, "Total Balance :- "+abc.getString(29), "MESSAGE" , JOptionPane.INFORMATION_MESSAGE);
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_jButton6ActionPerformed

